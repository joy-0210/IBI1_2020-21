# creat a function to compare the identical amino acid
def percent(s1,s2):
    number=0          #set initial number as zero
    for i in range(len(s1)):          #as len(s1)==len(s2), compare each amino acid
        if s1[i]==s2[i]:
            number+=1          #if the amino acid is same, add 1 to number
    return number/len(s1)*100          #calculate the percentage
    
# output the percentage
