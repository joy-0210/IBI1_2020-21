# create a function to compare different sequence and get the alignment score
def alignment(s1,s2):
    score=0          #set initial number as zero
    for i in range (len(s1)):          #use the blosum to calculate the score     
        score=score+blosum[amino.index(s1[i])][amino.index(s2[i])]
    return score
#output the score
